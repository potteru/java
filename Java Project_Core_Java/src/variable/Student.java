package variable;

public class Student {
	
	public static void main(String[] args) {
		
		// 1. Declaring Variable (Allowcating/Reserving Memory)
		String studentName;
		int studentRollNumber;
		char studentGrade;
		float studentMarks;
		
		//2. Initializing Variable ()
		studentName = "AdiYogi";
		studentRollNumber = 232;
		studentGrade = 'A';
		studentMarks = 85.235f;
				
				
		//3.Utilizing Variable
		
		System.out.println(studentName);
		System.out.println(studentRollNumber);
		System.out.println(studentGrade);
		System.out.println(studentMarks);
		
	}

}
