package java_star_pattern_programs;

public class Star_Pattern_I_Shape {

	public static void main(String[] args) {

		for(int i = 1; i<= 6; i++) {
			System.out.print(" * ");
		}
		for(int j = 2; j <= 6  ; j++) {
			System.out.println();
			System.out.print("        ");
			System.out.print("*");
		}
		System.out.println();
		for(int i = 1; i<= 6; i++) {
			System.out.print(" * ");
		}
	}

}
